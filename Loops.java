// looping statements //
//For loop //
import java.util.*;
 class conditional{
    public static void main ( String[] args){
        for(int i=0;i<=20;i++){
            System.out.println(i);
        }
    }
 }
         

// looping statements //
//Reverse for loop //
import java.util.*;
 class conditional{
    public static void main ( String[] args){
        for(int i=20;i>=0;i--){
            System.out.println(i);
        }
    }
 }


